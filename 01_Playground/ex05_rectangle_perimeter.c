#include <stdio.h>

int main (void)
{
    float wid, len, P;

    printf("Please enter the width of the rectangle: ");
    scanf ("%f", &wid);

    printf("Please enter the length of the rectangle: ");
    scanf("%f", &len);

    printf ("The rectangle's perimeter is: %f", P = 2 * wid * len);

    return 0;
}